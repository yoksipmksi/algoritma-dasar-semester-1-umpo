#include <iostream>
using namespace std;

int main() {
    int a = 7;
    a++;
    cout << "Nilai Variabel a: " << a << endl;

    int b =  7;
    ++b;
    cout << "Nilai Variabel b: " << b << endl;

    int c=  7;
    c--;
    cout << "Nilai Variabel c: " << c << endl;

    int d =  7;
    --d;
    cout << "Nilai Variabel d: " << d << endl;

    return 0;
}